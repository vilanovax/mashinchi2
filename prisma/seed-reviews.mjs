async function main() {
  console.log('🌱 Seeding car reviews...');

  // Fetch all cars from DB
  const cars = await prisma.car.findMany({ select: { id: true, nameEn: true } });
  console.log(`Found ${cars.length} cars in database`);

  let createdCount = 0;
  let skippedCount = 0;

  for (const car of cars) {
    const reviews = reviewsData[car.nameEn];
    if (!reviews) {
      console.warn(`  ⚠️  No reviews found for: ${car.nameEn}`);
      skippedCount++;
      continue;
    }

    for (const review of reviews) {
      await prisma.carReview.create({
        data: {
          carId:   car.id,
          source:  review.source,
          summary: review.summary,
          pros:    review.pros,
          cons:    review.cons,
          warnings: review.warnings,
          rating:  review.rating,
        },
      });
      createdCount++;
    }

    console.log(`  ✓ Created ${reviews.length} reviews for ${car.nameEn}`);
  }

  console.log('\n─────────────────────────────────────────');
  console.log(`✅ Done! Created ${createdCount} reviews total.`);
  if (skippedCount > 0) {
    console.log(`⚠️  Skipped ${skippedCount} cars (no review data found).`);
  }
}

main()
  .catch((e) => {
    console.error('Error seeding reviews:', e);
    process.exit(1);
  })
  .finally(async () => {
    await prisma.$disconnect();
    await pool.end();
  });
